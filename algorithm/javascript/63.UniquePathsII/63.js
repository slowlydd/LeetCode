/**
 * @param {number[][]} obstacleGrid
 * @return {number}
 */
var uniquePathsWithObstacles = function (obstacleGrid) {
  let m = obstacleGrid.length;
  let n = obstacleGrid[0].length;
  let array = [];
  for (let i = 0; i <= m; i++) {
    array[i] = [];
    for (let j = 0; j <= n; j++) {
      array[i][j] = -1;
    }
  }
  return dp(m - 1, n - 1, obstacleGrid, array);
};

function dp(m, n, obstacleGrid, array) {
  if (array[m][n] !== -1) {
    return array[m][n];
  }
  if (obstacleGrid[m][n] === 1) {
    array[m][n] = 0;
    return 0;
  } else {
    if (m === 0 && n === 0) {
      array[m][n] = 1;
      return 1;
    } else {
      if (m === 0) {
        array[0][n - 1] = dp(0, n - 1, obstacleGrid, array);
        return array[0][n - 1];
      }

      if (n === 0) {
        array[m - 1][0] = dp(m - 1, 0, obstacleGrid, array);
        return array[m - 1][0];
      }
      array[m][n - 1] = dp(m, n - 1, obstacleGrid, array);
      array[m - 1][n] = dp(m - 1, n, obstacleGrid, array);
      return array[m][n - 1] + array[m - 1][n];
    }
  }
}

